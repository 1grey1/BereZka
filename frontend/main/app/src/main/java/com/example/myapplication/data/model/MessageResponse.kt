package com.example.myapplication.data.model

/**
 * Универсальная модель ответа сервера с текстовым сообщением.
 *
 * Часто используется для:
 * - простых API-ответов (успех/ошибка);
 * - операций без возвращаемых данных (например: logout, delete, update);
 * - отображения уведомлений пользователю.
 */
data class MessageResponse(

    // Сообщение от сервера.
    // Может содержать:
    // - статус ("OK", "Success");
    // - текст ошибки;
    // - пояснение результата операции.
    val message: String
)