package com.example.myapplication.data.model

/**
 * Модель ответа сервера при успешной авторизации.
 *
 * Обычно приходит как JSON и автоматически десериализуется (например, через Retrofit + Gson/Kotlinx Serialization).
 */
data class LoginResponse(

    // Сообщение от сервера (например: "Login successful").
    // Может использоваться для отображения пользователю или логирования.
    val message: String,

    // Уникальный идентификатор пользователя в системе.
    // Часто используется для дальнейших запросов или хранения сессии.
    val user_id: Int,

    // Никнейм пользователя, подтверждённый сервером.
    // Может отличаться от введённого (например, если сервер нормализует данные).
    val nickname: String
)