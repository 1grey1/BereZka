package com.example.myapplication.data.model

/**
 * Модель запроса для авторизации/регистрации пользователя.
 *
 * Используется при отправке данных на сервер (например, через Retrofit).
 * Обычно сериализуется в JSON и передаётся в body HTTP-запроса.
 */
data class AuthRequest(

    // Никнейм (имя пользователя), введённый в форме.
    // Может использоваться как логин при авторизации.
    val nickname: String,

    // Пароль пользователя в открытом виде (до отправки на сервер).
    // На стороне сервера, как правило, хэшируется.
    val password: String
)